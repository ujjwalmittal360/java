class RelationalOperators{
    public static void main(String arg[]){

        int a=5;
        int b=6;

        // generally there are only 3 relation but we can make more using comnination of these 3
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a==b);

        // other relation 

        System.out.println(a!=b);
        System.out.println(a>=b);
        System.out.println(a<=b);


    }
}